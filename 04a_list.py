# Create a list using []
a = [1, 2 , 4, 56, 6]

# Print the list using print() function
print(a)

# Access using index using a[0], a[1], a[2]
print(a[2])

# Change the value of list using (mutable)
a[0] = 98
print(a)

# We can create a list with items of different types
c = [45, "Harry", False, 6.9]
print(c)

# we can change the entire list by doing
a=[ 1,2,3,"Razi"]
print(a)
# in the above example we have change the whole list

